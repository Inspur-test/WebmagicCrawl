public class HtmlUrlCrawlBean {

	public HtmlUrlCrawlBean(String child_urls,String url, String crawled, String last_crawl_time
			) {
		super();
		this.url = url;
		this.crawled = crawled;
		this.last_crawl_time = last_crawl_time;
		this.child_urls = child_urls;
	}
	public HtmlUrlCrawlBean(){
		super();
	}
	private String url;
	private String crawled;
	private String last_crawl_time;
	private String child_urls;
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getCrawled() {
		return crawled;
	}
	public void setCrawled(String crawled) {
		this.crawled = crawled;
	}
	public String getLast_crawl_time() {
		return last_crawl_time;
	}
	public void setLast_crawl_time(String last_crawl_time) {
		this.last_crawl_time = last_crawl_time;
	}
	public String getChild_urls() {
		return child_urls;
	}
	public void setChild_urls(String child_urls) {
		this.child_urls = child_urls;
	}
}

